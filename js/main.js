let currentLang = "en";

/* ── Language Switcher ── */

const langToggle = document.getElementById("langToggle");
const langDropdown = document.getElementById("langDropdown");
const langCurrentCode = document.getElementById("langCurrentCode");

function setLang(lang) {
  currentLang = lang;

  /* CJK font class */
  if (lang === "zh") {
    document.documentElement.classList.add("zh");
  } else {
    document.documentElement.classList.remove("zh");
  }

  /* Update dropdown button display */
  langCurrentCode.textContent = lang.toUpperCase();

  /* Mark active item */
  document.querySelectorAll(".lang-option").forEach((opt) => {
    opt.classList.toggle("active", opt.dataset.lang === lang);
  });

  /* Translate */
  const t = translations[lang];
  if (!t) return;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.dataset.i18n;
    if (t[key]) el.textContent = t[key];
  });

  /* Close dropdown */
  langDropdown.classList.remove("open");
  langToggle.classList.remove("open");
}

langToggle.addEventListener("click", (e) => {
  e.stopPropagation();
  const isOpen = langDropdown.classList.toggle("open");
  langToggle.classList.toggle("open", isOpen);
});

document.querySelectorAll(".lang-option").forEach((opt) => {
  opt.addEventListener("click", (e) => {
    e.stopPropagation();
    setLang(opt.dataset.lang);
  });
});

document.addEventListener("click", () => {
  langDropdown.classList.remove("open");
  langToggle.classList.remove("open");
});

/* ── Navbar scroll ── */

const navbar = document.getElementById("navbar");

function updateNav() {
  const scrollY = window.scrollY;
  navbar.classList.toggle("scrolled", scrollY > 40);

  const pageH = document.documentElement.scrollHeight - window.innerHeight;
  const ratio = pageH > 0 ? scrollY / pageH : 0;
  const threshold = 0.25;

  if (ratio < threshold) {
    navbar.classList.add("light-mode");
    navbar.classList.remove("dark-mode");
  } else {
    navbar.classList.add("dark-mode");
    navbar.classList.remove("light-mode");
  }
}

window.addEventListener("scroll", updateNav, { passive: true });
updateNav();

